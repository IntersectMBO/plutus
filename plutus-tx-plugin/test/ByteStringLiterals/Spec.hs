test_FromString :: TestTree
test_FromString = testCase "fromString" do
  for_ [0x00 .. 0xFF] \(i :: Int) -> do
    let s :: String = [chr i]
    fromBuiltin (fromString @BuiltinByteString s) @?= fromString @ByteString s

